package Chapter1.ch07;

public class DoubleTest {
    public static void main(String[] args) {
        double dNum = 3.14;
        float fNum = 3.14f;

        System.out.println("double = " + dNum); // 8바이트
        System.out.println("float = " + fNum);  // 4바이트, F, f 둘 다 가능하다.
        System.out.println("=================================");

        //
    }
}
